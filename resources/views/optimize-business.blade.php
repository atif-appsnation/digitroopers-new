						<h3 class="mt20 mb20">2 - Add Photos & Videos</h3>
						<p>Add your keywords in your photo’s names. Photos and videos of your local business will help to optimize your listing on Google but also to make visitors familiar with what your business is about and which products you deliver? Short Videos of 30 Seconds are very effective for marketing purposes. The good thing about video marketing is that nowadays, you can easily create them, by using smartphone apps or a free online video editing app.</p>
						
						<h3 class="mt20 mb20">3 - Earn Positive Online Reviews</h3>
						<p>Online reviews are incredibly valuable as social proof to help your business stand out among other search results. Positive reviews inspire customers to trust a business more than others, so it’s important to ask your happiest customers to help you out and leave an honest review of their experience with you.</p>
                        <div class="image-set mt20 mb20"><img src="images/blog/google-my-business-reviews.webp" alt="blog images" class="img-fluid d-block mx-auto" /></div>

						<h3 class="mt20 mb20">4 - Use Google my Business Posts</h3>
						<p>Another important thing that you can do to improve your ranking is to use Google Post which appears below your listing. These posts act as ads with an image, some text, and a call to action button from where the visitor can be directed to your website or landing page.</p>
						<p>You can use it to launch a new product, to promote offers for existing products, and whatever else you want to do for example announcing future events.</p>
						<div class="image-set mt20 mb20"><img src="images/blog/google-my-business-post.webp" alt="blog images" class="img-fluid d-block mx-auto" /></div>
						
