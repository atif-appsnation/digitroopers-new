						<h3 class="mt20 mb20">1 - Business Description</h3>
						<p>Make sure you input your business description including information such as Business Name, Category, opening hours, Street Address, Email Address, Contact Details, types of payments accepted, and so on.</p>
                        <p>The more information you share the better. In your business description consider that you only have 750 characters of which only 250 show up on the Google Search Result Page so make sure you write a solid and sharp description within the first 250 characters.</p>
