						<p>Anyone can ask a question directly from the search results and you can answer the question right there and then, which is an excellent best opportunity to know the customer’s requirements and win the trust of other potential customers through your customer-oriented responses. To improve this practice, you can add Google My Business App on your mobile device/ handphone and get the notifications so that you could answer them immediately and get many leads.</p>
						
